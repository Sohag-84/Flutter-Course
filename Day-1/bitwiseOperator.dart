void main() {
  //bitwise operator in dart:

  int num1 = 30;
  int num2 = 10;

  //bitwise OR
  print("num1 | num2 = ${num1 | num2}");

  //bitwise AND
  print("num1 & num2 = ${num1 & num2}");

  //bitwise XOR
  print("num1 ^ num2 = ${num1 ^ num2}");

  //bitwise binary 1's complement
  print("~num1 = ${~num1}");
}
